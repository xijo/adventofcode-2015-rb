class Gift
  def initialize(lwh)
    @l, @w, @h = lwh.split('x').map(&:to_i)
  end

  def paper
    2*@l*@w + 2*@w*@h + 2*@h*@l + smallest_area
  end

  def smallest_area
    a, b = shortest_sides
    a * b
  end

  def shortest_sides
    [@l, @w, @h].sort.reverse.pop(2)
  end

  def ribbon
    shortest_sides.inject(0) { |m, v| m + 2*v } + (@l * @w * @h)
  end
end


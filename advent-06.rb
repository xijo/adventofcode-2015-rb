class Grid
  def initialize
    @grid = Hash.new(0)
  end

  def execute(line)
    command, area = line.split(/(\d.*)/)
    start, finish = area.split(' through ').map { |v| v.split(',').map(&:to_i) }
    execute_command(command.strip, start, finish)
  end

  def execute_command(command, start, finish)
    case command
    when 'turn on' then turn_on(start, finish)
    when 'turn off' then turn_off(start, finish)
    when 'toggle' then toggle(start, finish)
    else raise "unknown command #{command.inspect}"
    end
  end

  def lit_count
    @grid.values.count(true)
  end

  def total_brightness
    @grid.values.inject(0) { |m, v| m + v }
  end

  def turn_on(start, finish)
    every_position(start, finish) do |position|
      @grid[position] += 1
    end
  end

  def turn_off(start, finish)
    every_position(start, finish) do |position|
      @grid[position] = [@grid[position] - 1, 0].max
    end
  end

  def toggle(start, finish)
    every_position(start, finish) do |position|
      @grid[position] += 2
    end
  end

  def every_position(start, finish, &block)
    start[0].upto(finish[0]).each do |x|
      start[1].upto(finish[1]).each do |y|
        position = "#{x},#{y}"
        block.call(position)
      end
    end
  end
end


